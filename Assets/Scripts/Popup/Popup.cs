using TMPro;
using UnityEngine;

public class Popup : MonoBehaviour
{
    // ----------------------------------------------------------------------------------------------------------------------------------
    // Serialize Fields
    // ----------------------------------------------------------------------------------------------------------------------------------

    [SerializeField] private TMP_Text m_text;

    [SerializeField] private float m_moveSpeed = 1.5f;
    [SerializeField] private float m_fadeSpeed = 2f;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Runtime States
    // ----------------------------------------------------------------------------------------------------------------------------------

    private Color m_color;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Setting Up Popup
    // ----------------------------------------------------------------------------------------------------------------------------------

    public void Setup(string text, Color color)
    {
        m_text.text = text;
        m_color = color;
    }

    public void Setup(int value, Color color)
    {
        m_text.text = value.ToString();
        m_color = color;
    }

    // ----------------------------------------------------------------------------------------------------------------------------------
    // MonoBehaviour Events
    // ----------------------------------------------------------------------------------------------------------------------------------

    private void Update()
    {
        MoveUp();
        FadeOut();
    }

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Helper Functions
    // ----------------------------------------------------------------------------------------------------------------------------------

    private void MoveUp()
    {
        transform.position += Vector3.up * m_moveSpeed * Time.deltaTime;
    }

    private void FadeOut()
    {
        m_color.a -= m_fadeSpeed * Time.deltaTime;
        m_text.color = m_color;

        if (m_color.a <= 0f)
        {
            Destroy(gameObject);
        }
    }
}