using TMPro;
using UnityEngine;

public class ShopButton : MonoBehaviour
{
    // ----------------------------------------------------------------------------------------------------------------------------------
    // Serialize Fields
    // ----------------------------------------------------------------------------------------------------------------------------------

    [SerializeField] private TextMeshProUGUI m_text;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Runtime States
    // ----------------------------------------------------------------------------------------------------------------------------------

    private ShopUI m_owner;
    private int m_price;
    private InventoryItemData m_itemData;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Setup
    // ----------------------------------------------------------------------------------------------------------------------------------

    public void Setup(ShopUI owner, InventoryItemData itemData, int price)
    {
        if (itemData == null)
        {
            Destroy(gameObject);
            return;
        }

        m_owner = owner;
        m_itemData = itemData;
        m_price = price;

        m_text.text = $"{itemData.displayName} - ${price}";
    }

    // ----------------------------------------------------------------------------------------------------------------------------------
    // On Click (Buy)
    // ----------------------------------------------------------------------------------------------------------------------------------

    public void Buy()
    {
        m_owner.ConsumerTryBuy(m_itemData, m_price);
    }
}
