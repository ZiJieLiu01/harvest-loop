using TMPro;
using UnityEngine;

public class DebugOverlayUI : MonoBehaviour
{
    // ----------------------------------------------------------------------------------------------------------------------------------
    // Seralize Fields
    // ----------------------------------------------------------------------------------------------------------------------------------

    [Header("UI")]
    [SerializeField] private TextMeshProUGUI m_text;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Runtime States
    // ----------------------------------------------------------------------------------------------------------------------------------

    private Tile m_currentTile;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // MonoBehaviour Events
    // ----------------------------------------------------------------------------------------------------------------------------------

    private void Start()
    {
        HandleToggle(false);
    }

    private void OnEnable()
    {
        DebugManager.Instance.OnDebugToggled += HandleToggle;
    }

    private void Update()
    {
        if (!gameObject.activeSelf)
            return;

        UpdateText();
    }

    // ----------------------------------------------------------------------------------------------------------------------------------
    // UI Update
    // ----------------------------------------------------------------------------------------------------------------------------------

    private void UpdateText()
    {
        m_currentTile = TileManager.Instance.CurrHoverTile;

        if (m_text == null || m_currentTile == null)
            return;

        m_text.text = m_currentTile.GetDebugInfo();
    }

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Toggle
    // ----------------------------------------------------------------------------------------------------------------------------------

    private void HandleToggle(bool show)
    {
        gameObject.SetActive(show);
    }
}
