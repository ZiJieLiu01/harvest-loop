using System.Collections.Generic;
using UnityEngine;


public class TileDatabase : MonoBehaviour
{
    // ----------------------------------------------------------------------------------------------------------------------------------
    // Singleton
    // ----------------------------------------------------------------------------------------------------------------------------------

    public static TileDatabase Instance;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Serialize Fields
    // ----------------------------------------------------------------------------------------------------------------------------------

    [SerializeField] private TileData[] m_allTiles;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Runtime States
    // ----------------------------------------------------------------------------------------------------------------------------------

    private Dictionary<TileType, TileData> m_tileLookup;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // MonoBehaviour Events
    // ----------------------------------------------------------------------------------------------------------------------------------

    public TileData GetTileData(TileType type)
    {
        if (m_tileLookup.TryGetValue(type, out TileData itemData))
            return itemData;

        Debug.LogWarning($"TileData for Type {type} not found!");
        return null;
    }

    public int DataCount => m_allTiles.Length;  // TEMP
    public TileData[] AllTileData => m_allTiles;    // TEMP
}
