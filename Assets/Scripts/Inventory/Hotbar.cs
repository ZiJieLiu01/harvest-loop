using System;
using UnityEngine;

public class Hotbar : MonoBehaviour
{
    // ----------------------------------------------------------------------------------------------------------------------------------
    // Serialize Fields
    // ----------------------------------------------------------------------------------------------------------------------------------

    [SerializeField] private Inventory m_inventory;
    [SerializeField] private int m_hotbarSize = 9;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Action Events
    // ----------------------------------------------------------------------------------------------------------------------------------

    public Action<int> OnSelectIndexChange;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Runtime States
    // ----------------------------------------------------------------------------------------------------------------------------------

    private int m_selectedIndex = 0;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Hotbar Logics
    // ----------------------------------------------------------------------------------------------------------------------------------

    public void Select(int index)
    {
        if (index < 0 || index >= m_hotbarSize)
            return;

        m_selectedIndex = index;
        OnSelectIndexChange?.Invoke(m_selectedIndex);
    }

    public ItemInstance GetSelectedItem()
    {
        return m_inventory.GetItem(m_selectedIndex);
    }

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Getters / Setters
    // ----------------------------------------------------------------------------------------------------------------------------------

    public Inventory Inventory => m_inventory;
}
