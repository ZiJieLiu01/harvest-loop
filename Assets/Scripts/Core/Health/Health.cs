using System;
using UnityEngine;

public class Health : MonoBehaviour, IDamageable
{
    // ----------------------------------------------------------------------------------------------------------------------------------
    // Seralize Fields
    // ----------------------------------------------------------------------------------------------------------------------------------

    [SerializeField] private int m_maxHealth = 10;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Action Events
    // ----------------------------------------------------------------------------------------------------------------------------------

    public Action OnDeath;
    public Action<int, Vector2> OnDamaged;  // int = damage taken, Vector2 = knockback
    public static Action<int, Vector3> OnAnyDamage; // int = damage taken, Vector3 = position

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Runtime States
    // ----------------------------------------------------------------------------------------------------------------------------------

    private int m_currentHealth;
    private Transform m_transform;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // MonoBehaviour Events
    // ----------------------------------------------------------------------------------------------------------------------------------

    private void Awake()
    {
        m_currentHealth = m_maxHealth;
        m_transform = transform;
    }

    // ----------------------------------------------------------------------------------------------------------------------------------
    // IDamageable Implementation
    // ----------------------------------------------------------------------------------------------------------------------------------

    public void TakeDamage(int damage, Vector2 knockback)
    {
        m_currentHealth -= damage;
        OnDamaged?.Invoke(damage, knockback);
        OnAnyDamage?.Invoke(damage, m_transform.position);

        if (m_currentHealth <= 0)
            Die();
    }

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Helper Functions
    // ----------------------------------------------------------------------------------------------------------------------------------

    private void Die()
    {
        OnDeath?.Invoke();
    }
}
