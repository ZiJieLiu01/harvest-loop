using System.Collections.Generic;
using UnityEngine;
using UnityEngine.InputSystem;

public class CropManager : MonoBehaviour
{
    // ----------------------------------------------------------------------------------------------------------------------------------
    // Singleton
    // ----------------------------------------------------------------------------------------------------------------------------------

    public static CropManager Instance;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Serialize Fields
    // ----------------------------------------------------------------------------------------------------------------------------------

    [SerializeField] private CropInstance m_cropPrefab;

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Runtime States
    // ----------------------------------------------------------------------------------------------------------------------------------

    private HashSet<CropInstance> m_cropsPlanted = new();

    // ----------------------------------------------------------------------------------------------------------------------------------
    // MonoBehaviour Events
    // ----------------------------------------------------------------------------------------------------------------------------------

    private void Awake()
    {
        if(Instance != null && Instance != this)
        {
            Destroy(gameObject);
            return;
        }

        Instance = this;
    }

    private void Start()
    {
        TimeManager.Instance.OnDayChange += OnNewDay;
    }

    // ----------------------------------------------------------------------------------------------------------------------------------
    // Planting Crops
    // ----------------------------------------------------------------------------------------------------------------------------------

    public void AddCrop(CropInstance crop)
    {
        m_cropsPlanted.Add(crop);
    }

    public void RemoveCrop(CropInstance crop)
    {
        m_cropsPlanted.Remove(crop);
    }

    public void OnNewDay(int day)
    {
        foreach (var crop in m_cropsPlanted)
        {
            crop.OnNewDay();
        }
    }
}
